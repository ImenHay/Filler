#include "libft.h"

void	ft_putnbr(int nb)
{
	int i;

	i = 0;
	if (nb == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		nb = 147483648;
	}
	if (nb < 0)
	{
		nb = nb * (-1);
		ft_putchar('-');
	}
	i = nb;
	if (i >= 10)
	{
		ft_putnbr(i / 10);
		ft_putchar(i % 10 + 48);
	}
	if (i < 10)
		ft_putchar(i + 48);
}
