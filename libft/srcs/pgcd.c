#include "libft.h"

int		pgcd(int a, int b)
{
	int tmp;
	int i;

	i = 0;
	tmp = 0;
	if (a == 1 || b == 1)
		return (1);
	if (a > b)
	{
		tmp = a;
		a = b;
		b = tmp;
	}
	i = a;
	while (i >= 1)
	{
		if ((a % i == 0) && (b % i == 0))
			return (i);
		i--;
	}
	return (1);
}
